%--------------------fix----------------------------
%fix:fix()内の要素が0に近くなるようにに丸める

fixtest=[-5.4 6.3; 10.8 -9.24; 3.5 6.23];
fixtest2=[-1.25+0.5i 2.52-3.3i; 5.3+10.6i 0.2-0.1i];
disp(fix(fixtest));
disp(fix(fixtest2));

