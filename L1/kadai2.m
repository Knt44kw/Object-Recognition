%round: 四捨五入をして整数または少数(第二引数を指定して)に丸める.
roundtest=[1.5,2.2,3.4,4.8,5.91];
roundtest2=[1.53,2.27,3.56,4.14,5.96];
roundtest3=[5.874,8.456,10.48,3.212,6.751];
disp(round(roundtest));
disp(round(roundtest2,1));%小数点第1位で丸める
%有効数字3桁で丸める
%左端の桁から丸めるには'siginificant'を指定する
disp(round(roundtest3,3,'significant'))

